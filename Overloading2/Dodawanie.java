
public class Dodawanie {

	int dodaj(int a, int b, int c) {
		System.out.println("Suma trzech int typu int:");
		return a+b+c;
	}
	
	double dodaj(int b, int c) {
		System.out.println("Suma dwóch int typu double:");
		return b+c;
	}
	
	double dodaj(double a, double b, double c) {
		System.out.println("Suma trzech double typu double:");
		return a+b+c;
	}
	
}
